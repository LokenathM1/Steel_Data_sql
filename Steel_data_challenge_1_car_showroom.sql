from sales as s
join cars as c
join salespersons as sp
on s.car_id = c.car_id and s.salesman_id = sp.salesman_id;

/*5. What is the total revenue generated by each car type?*/

select c.make as Company , c.type as Model , sum(c.cost_$)as Revenue
from sales as s
join cars as c
on s.car_id = c.car_id
group by c.make , c.type
order by Revenue desc;

