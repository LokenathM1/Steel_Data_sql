/*1. What are the details of all cars purchased in the year 2022?*/
select s.car_id , c.make , c.type , c.style , c.cost_$,  s.purchase_date
from sales as s
join cars as c
join salespersons as sp
on s.car_id = c.car_id and s.salesman_id = sp.salesman_id
where year(purchase_date) = 2022
order by purchase_date asc;

/*2. What is the total number of cars sold by each salesperson?*/
select  sp.name as Salespersons , count(s.sale_id) as Units_Sold
from sales as s
join salespersons as sp
on s.salesman_id = sp.salesman_id
group by s.salesman_id;

/*3. What is the total revenue generated by each salesperson?*/
select  sp.name as Salespersons , sum(c.cost_$) as Revenue
from sales as s
join salespersons as sp
join cars as c
on s.salesman_id = sp.salesman_id and s.car_id = c.car_id
group by sp.name
order by Revenue desc;

/*4. What are the details of the cars sold by each salesperson?*/
